  gfx->fillCircle(tailSX, tailSY, 4, COLOR_WATCH_SEC);

  // Center hub - layered for depth
  gfx->fillCircle(cx, cy, 6, 0x4208);
  gfx->fillCircle(cx, cy, 4, COLOR_WATCH_MARKER);
  gfx->fillCircle(cx, cy, 2, COLOR_WATCH_SEC);
